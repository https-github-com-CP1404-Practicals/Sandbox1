# numbers = [3, 1, 4, 1, 5, 9, 2]

# What values do the following expressions have?

# 1. numbers[0] - returns the first element of the list: 3
# 2. numbers[-1] - returns the last element of the list: 2
# 3. numbers[3] - returns the element at index 3 of the list: 1
# 4. numbers[:-1] - returns all elements of the list except the last one: [3, 1, 4, 1, 5, 9]
# 5. numbers[3:4] - returns a new list containing only the element at index 3: [1]
# 6. 5 in numbers - returns True because the list contains the value 5
# 7. 7 in numbers - returns False because the list does not contain the value 7
# 8. "3" in numbers - returns False because the list contains the integer 3, not the string "3"
# 9. numbers + [6, 5, 3] - returns a new list that concatenates the original list with the given list: [3, 1, 4, 1, 5, 9, 2, 6, 5, 3]
